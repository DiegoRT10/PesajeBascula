     private void getPuerto(){
    
    while(comports.hasMoreElements()){
    cpi = (CommPortIdentifier) comports.nextElement();
    if(cpi.getName().equals("COM2")){
        break;
    }
    }
    
        try {
            CommPort puerto = cpi.open("Puerto Serial",5000);
            puertoSerial = (SerialPort) puerto;
            puertoSerial.setSerialPortParams(19200, SerialPort.DATABITS_8, SerialPort.STOPBITS_1, SerialPort.PARITY_NONE);
            entrada = new Scanner(puertoSerial.getInputStream());
            salida = new PrintStream(puertoSerial.getOutputStream());
        } catch (PortInUseException ex) {
            lblInfoEstado.setText("Puerto en uso");
            //Logger.getLogger(pesaje.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            lblInfoEstado.setText("Error de lectura");
            //Logger.getLogger(pesaje.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedCommOperationException ex) {
            lblInfoEstado.setText("Error de operacion");
            //Logger.getLogger(pesaje.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        while(!entrada.hasNextDouble()){
            entrada.close();
            entrada = null;
        try {
            entrada = new Scanner(puertoSerial.getInputStream());
            salida = new PrintStream(puertoSerial.getOutputStream());
        } catch (IOException ex) {
            lblInfoEstado.setText("Error de lectura");
            //Logger.getLogger(pesaje.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
        peso = entrada.nextDouble();
        txtaPesos.append(peso.toString()+System.getProperty("line.separator"));
        System.out.println("Peso: "+peso);
       
        if(pesoOld.equals(peso)){
             contador++;
             System.out.println("contador mas"+contador);
         }else{contador=0;
        System.out.println("contador menos"+contador);}
         
         if(contador==10){
             lblPesoTotal.setText(peso.toString());
             MostrarLabels(false);
             lblDescripcion2.setVisible(true);
        lblPesoTotal.setVisible(true);
         }
         pesoOld=peso;
        
        
//        peso=0.0;
        //salida.println("Dato capturado");
        
    }
     
